function calculadora(){
    var num1 = prompt('Digite um número: ');
    var num2 = prompt('Digite outro número: ');
    var operacao = prompt('Digite a operação: ');
    var resultado;

    switch(operacao){
        case '+':{
            resultado == num1 + num2;
            alert('O resultado é : '+resultado);
            break;
        }
    }
}
calculadora();